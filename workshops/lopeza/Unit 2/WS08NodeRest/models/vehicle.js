const mongoose = require('mongoose')

const vehiclesSchema = new mongoose.Schema({
  plateId: {
    type: String,
    required: true,
  },
  brand: {
    type: String,
    required: true,
  },
  model: {
    type: String,
    required: true,
  },
  year: {
    type: Number,
    required: true,
  },
  color: {
    type: String,
    required: true,
  },
  
})

module.exports = mongoose.model('Vehicles', vehiclesSchema)